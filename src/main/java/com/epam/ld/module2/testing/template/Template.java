package com.epam.ld.module2.testing.template;

/**
 * The type Template.
 */
public class Template {
    private String name;
    private String surname;

    public Template(String name, String surname) {
        this.name = name;
        this.surname = surname;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }
}
